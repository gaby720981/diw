#El método center() genera una copia de la cadena original, tratando de centrarla dentro de un campo de un ancho especificado.

# Demostración del método center():
print('[' + 'alpha'.center(20) + ']')

print('[' + 'Beta'.center(2) + ']')
print('[' + 'Beta'.center(4) + ']')
print('[' + 'Beta'.center(6) + ']')

print('[' + 'gamma'.center(20, '*') + ']')
