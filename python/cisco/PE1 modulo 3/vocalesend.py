

from ast import Continue

user_word = (input ("Ingrese una palabra: ")).upper()


for letter in user_word:
    if letter == "A":
        Continue
    elif letter == "E":
        Continue
    elif letter == "I":
        Continue
    elif letter == "O":
        Continue
    elif letter == "U":
        Continue
    else:
        print(letter, end="")