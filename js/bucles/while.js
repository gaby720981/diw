// Primero se declara una variable i con un valor de 200.
// El bucle while continúa mientras i sea menor o igual que 250.
// Dentro del bucle, el número actual de la iteración se muestra en la consola usando console.log().
// Luego, se agrega 2 a la variable i antes de continuar con la siguiente iteración del bucle.

let i = 200;
while (i <= 250) {
  console.log(i);
  i += 2;
}
