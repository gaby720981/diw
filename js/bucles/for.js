// El bucle for inicia en la variable i con un valor de 200 y continúa mientras i sea menor o igual que 250.
// En cada iteración del bucle, se agrega 2 a la variable i.
// El número actual de la iteración se muestra en la consola usando console.log().


for (let i = 200; i <= 250; i += 2) {
    console.log(i);
  }
  